package wheels2.model;

public class Team {
	private String empName;
	private String empDesignation;
	private String empPicture;
	private String empFbUrl;
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpDesignation() {
		return empDesignation;
	}
	public void setEmpDesignation(String empDesignation) {
		this.empDesignation = empDesignation;
	}
	public String getEmpPicture() {
		return empPicture;
	}
	public void setEmpPicture(String empPicture) {
		this.empPicture = empPicture;
	}
	public String getEmpFbUrl() {
		return empFbUrl;
	}
	public void setEmpFbUrl(String empFbUrl) {
		this.empFbUrl = empFbUrl;
	}
	
}
